/**
 * CREATE Script for init of DB
 */


drop table driver;

create table driver (id INT PRIMARY KEY AUTO_INCREMENT NOT NULL,
                     date_created TIMESTAMP AS CURRENT_TIMESTAMP NOT NULL,
                     deleted BOOLEAN, online_status VARCHAR2(10), username VARCHAR2(256),password VARCHAR2(256),
                     coordinate NVARCHAR2, date_coordinate_updated TIMESTAMP );

drop TABLE car;
create table car (
  car_id INT AUTO_INCREMENT NOT NULL,
  license_plate VARCHAR2(10) PRIMARY KEY NOT NULL ,
  driver_id INT ,
  date_created TIMESTAMP AS CURRENT_TIMESTAMP NOT NULL,
  deleted BOOLEAN NOT NULL ,
  available BOOLEAN NOT NULL ,
  seat_count INT ,
  convertible BOOLEAN,
  rating INT ,
  engine_type VARCHAR2(20));

